package dev.dhanika.rouge.build;

import dev.dhanika.rouge.build.BuildSpec.BlockEntry;
import net.minecraft.nbt.CompoundTag;
import net.minecraft.nbt.ListTag;
import net.minecraft.nbt.LongArrayTag;
import net.minecraft.nbt.NbtIo;
import net.minecraft.nbt.Tag;

import java.io.OutputStream;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;

/**
 * Writes a list of blocks to a Litematica {@code .litematic} file (gzipped NBT).
 * <p>
 * The format is a block-state palette plus a bit-packed long array of palette
 * indices. We replicate Litematica's own {@code LitematicaBitArray} packing
 * exactly (entries may span across longs; {@code bits = max(2, ceil(log2(size)))};
 * position order {@code y*W*L + z*W + x}; palette index 0 is always air).
 *
 * @see <a href="https://github.com/maruohon/litematica">Litematica</a>
 */
public final class LitematicWriter {

    /** Minecraft 1.20.1 data version. */
    private static final int MC_DATA_VERSION = 3465;
    /** Litematica schematic format version for 1.13+. */
    private static final int SCHEMATIC_VERSION = 6;
    private static final String AIR = "minecraft:air";

    private LitematicWriter() {
    }

    /**
     * Writes {@code blocks} (a subset of a solution) as a single-region schematic.
     *
     * @param name    schematic + region name shown in Litematica
     * @param blocks  blocks to include (local coords)
     * @param sizeX   bounding size along X
     * @param sizeY   bounding size along Y
     * @param sizeZ   bounding size along Z
     * @param outFile destination {@code .litematic} path
     */
    public static void write(String name, List<BlockEntry> blocks,
                             int sizeX, int sizeY, int sizeZ, Path outFile) throws Exception {
        final int volume = sizeX * sizeY * sizeZ;

        // --- Palette: index 0 = air, then each unique block state. ---
        Map<String, Integer> indexByState = new LinkedHashMap<>();
        ListTag paletteTag = new ListTag();
        indexByState.put(AIR, 0);
        paletteTag.add(stateTag(parse(AIR)));

        // --- Block index per position (defaults to 0 = air). ---
        int[] indices = new int[volume];
        int placed = 0;
        for (BlockEntry b : blocks) {
            if (b.x() < 0 || b.y() < 0 || b.z() < 0
                    || b.x() >= sizeX || b.y() >= sizeY || b.z() >= sizeZ) {
                continue; // out of declared bounds — skip defensively
            }
            ParsedState ps = parse(b.block());
            String key = ps.key();
            Integer idx = indexByState.get(key);
            if (idx == null) {
                idx = paletteTag.size();
                indexByState.put(key, idx);
                paletteTag.add(stateTag(ps));
            }
            indices[posIndex(b.x(), b.y(), b.z(), sizeX, sizeZ)] = idx;
            placed++;
        }

        // --- Bit-pack the indices. ---
        int bits = Math.max(2, 32 - Integer.numberOfLeadingZeros(Math.max(1, paletteTag.size() - 1)));
        LitematicaBitArray bitArray = new LitematicaBitArray(bits, volume);
        for (int i = 0; i < volume; i++) {
            bitArray.set(i, indices[i]);
        }

        // --- Region compound. ---
        CompoundTag region = new CompoundTag();
        region.put("Position", posTag(0, 0, 0));
        region.put("Size", posTag(sizeX, sizeY, sizeZ));
        region.put("BlockStatePalette", paletteTag);
        region.put("BlockStates", new LongArrayTag(bitArray.toArray()));
        region.put("TileEntities", new ListTag());
        region.put("Entities", new ListTag());
        region.put("PendingBlockTicks", new ListTag());
        region.put("PendingFluidTicks", new ListTag());

        CompoundTag regions = new CompoundTag();
        regions.put(name, region);

        // --- Metadata. ---
        long now = System.currentTimeMillis();
        CompoundTag metadata = new CompoundTag();
        metadata.putString("Name", name);
        metadata.putString("Author", "Rouge");
        metadata.putString("Description", "Generated by Rouge");
        metadata.putInt("RegionCount", 1);
        metadata.put("EnclosingSize", posTag(sizeX, sizeY, sizeZ));
        metadata.putInt("TotalVolume", volume);
        metadata.putInt("TotalBlocks", placed);
        metadata.putLong("TimeCreated", now);
        metadata.putLong("TimeModified", now);

        // --- Root. ---
        CompoundTag root = new CompoundTag();
        root.putInt("MinecraftDataVersion", MC_DATA_VERSION);
        root.putInt("Version", SCHEMATIC_VERSION);
        root.put("Metadata", metadata);
        root.put("Regions", regions);

        Files.createDirectories(outFile.getParent());
        try (OutputStream out = Files.newOutputStream(outFile)) {
            NbtIo.writeCompressed(root, out);
        }
    }

    /** Litematica position order: y outermost, then z, then x. */
    private static int posIndex(int x, int y, int z, int sizeX, int sizeZ) {
        return (y * sizeX * sizeZ) + (z * sizeX) + x;
    }

    private static CompoundTag posTag(int x, int y, int z) {
        CompoundTag t = new CompoundTag();
        t.putInt("x", x);
        t.putInt("y", y);
        t.putInt("z", z);
        return t;
    }

    private static CompoundTag stateTag(ParsedState ps) {
        CompoundTag t = new CompoundTag();
        t.putString("Name", ps.name());
        if (!ps.props().isEmpty()) {
            CompoundTag props = new CompoundTag();
            for (Map.Entry<String, String> e : ps.props().entrySet()) {
                props.putString(e.getKey(), e.getValue());
            }
            t.put("Properties", props);
        }
        return t;
    }

    // ---- block-state string parsing: "minecraft:repeater[facing=east,delay=1]" ----

    private record ParsedState(String name, LinkedHashMap<String, String> props) {
        /** Canonical dedupe key: name + properties in declaration order. */
        String key() {
            return props.isEmpty() ? name : name + props;
        }
    }

    private static ParsedState parse(String block) {
        int b = block.indexOf('[');
        if (b < 0) {
            return new ParsedState(block, new LinkedHashMap<>());
        }
        String name = block.substring(0, b);
        int end = block.indexOf(']', b);
        String inner = block.substring(b + 1, end < 0 ? block.length() : end);
        LinkedHashMap<String, String> props = new LinkedHashMap<>();
        for (String pair : inner.split(",")) {
            String p = pair.trim();
            if (p.isEmpty()) {
                continue;
            }
            int eq = p.indexOf('=');
            if (eq > 0) {
                props.put(p.substring(0, eq).trim(), p.substring(eq + 1).trim());
            }
        }
        return new ParsedState(name, props);
    }

    /**
     * Bit-packed array matching Litematica's {@code LitematicaBitArray}: each entry
     * occupies {@code bits} contiguous bits and may span two longs.
     */
    static final class LitematicaBitArray {
        private final long[] data;
        private final int bits;
        private final long mask;

        LitematicaBitArray(int bits, int size) {
            this.bits = bits;
            this.mask = (1L << bits) - 1L;
            this.data = new long[(int) (((long) size * bits + 63L) / 64L)];
        }

        void set(int index, int value) {
            long v = value & mask;
            long bitIndex = (long) index * bits;
            int startLong = (int) (bitIndex >> 6);
            int startBit = (int) (bitIndex & 63L);
            int endLong = (int) (((long) (index + 1) * bits - 1L) >> 6);
            data[startLong] |= v << startBit;
            if (startLong != endLong) {
                data[endLong] |= v >>> (64 - startBit);
            }
        }

        long[] toArray() {
            return data;
        }
    }
}
